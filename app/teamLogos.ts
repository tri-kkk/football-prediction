// TEAM_LOGOS: API-Football 기반 URL 매핑
export const TEAM_LOGOS: Record<string, string> = (() => {
  const logos: Record<string, string> = {}
  
  // API_FOOTBALL_TEAM_IDS의 모든 팀에 대해 URL 생성
  for (const [teamName, teamId] of Object.entries(API_FOOTBALL_TEAM_IDS)) {
    logos[teamName] = `https://media.api-sports.io/football/teams/${teamId}.png`
  }
  
  // 한글 팀명도 추가 (영문 팀명과 동일한 로고)
  for (const [englishName, koreanName] of Object.entries(TEAM_NAME_KR)) {
    const teamId = API_FOOTBALL_TEAM_IDS[englishName]
    if (teamId) {
      logos[koreanName] = `https://media.api-sports.io/football/teams/${teamId}.png`
    }
  }
  
  return logos
})()

// 팀 로고 가져오기 함수 (스마트 매칭)
export function getTeamLogo(teamName: string): string {
  if (!teamName) {
    return 'https://via.placeholder.com/48x48/1e293b/ffffff?text=TEAM'
  }
  
  // Step 1: 정확한 매칭
  if (TEAM_LOGOS[teamName]) {
    return TEAM_LOGOS[teamName]
  }
  
  // Step 2: 대소문자 무시 매칭
  const normalizedName = teamName.toLowerCase().trim()
  for (const [key, value] of Object.entries(TEAM_LOGOS)) {
    if (key.toLowerCase().trim() === normalizedName) {
      return value
    }
  }
  
  // Step 3: 부분 매칭
  for (const [key, value] of Object.entries(TEAM_LOGOS)) {
    const keyLower = key.toLowerCase()
    
    if (keyLower.includes(normalizedName) && normalizedName.length >= 4) {
      return value
    }
    if (normalizedName.includes(keyLower) && keyLower.length >= 4) {
      return value
    }
  }
  
  // Step 4: 실패 시 placeholder
  const initials = teamName
    .split(' ')
    .map(word => word[0])
    .join('')
    .substring(0, 3)
    .toUpperCase()
  
  console.warn(`⚠️ 로고 없음: "${teamName}"`)
  return `https://via.placeholder.com/48x48/1e293b/ffffff?text=${encodeURIComponent(initials)}`
}